// comparision operators  (==,!=,<=,>=,<,>)

#include <iostream>
using namespace std;
int main()
{

    int a = 6;
    int b = 7;

    cout << "a == b = " << (a == b) << endl;
    cout << "a != b = " << (a != b) << endl;
    cout << "a <= b = " << (a <= b) << endl;
    cout << "a >= b = " << (a > b) << endl;
    cout << "a > b = " << (a > b) << endl;
    cout << "a < b= " << (a < b) << endl;

    return 0;
}